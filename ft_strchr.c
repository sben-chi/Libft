#include "libft.h"

char	*ft_strchr(char const *s, int c)
{
	int		i;

	i = 0;
	while (1)
	{
		if (s[i] == (char)c)
			return (&((char *)s)[i]);
		else if (!s[i])
			break ;
		i++;
	}
	return (NULL);
}
